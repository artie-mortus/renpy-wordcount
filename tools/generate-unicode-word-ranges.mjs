import fs from 'node:fs';

const isWord = /[\p{L}\p{N}]/u;
const ranges = [];
let start = null;
let previous = null;
for (let point = 0; point <= 0x10ffff; point += 1) {
  const matches = isWord.test(String.fromCodePoint(point));
  if (matches && start === null) start = point;
  if (matches) previous = point;
  if (!matches && start !== null) {
    ranges.push([start, previous]);
    start = null;
  }
}
if (start !== null) ranges.push([start, previous]);

const rows = ranges.map(([first, last]) => `    {0x${first.toString(16)}, 0x${last.toString(16)}},`).join('\n');
const output = `// Generated by tools/generate-unicode-word-ranges.mjs using Node ${process.versions.node}
// Unicode property escapes use the Unicode data bundled with that Node release.
#pragma once
#include <cstdint>
#include <utility>

namespace say_count::unicode_data {
inline constexpr std::pair<std::uint32_t, std::uint32_t> word_ranges[] = {
${rows}
};
}  // namespace say_count::unicode_data
`;
fs.writeFileSync(new URL('../src/core/unicode_word_ranges.h', import.meta.url), output);
